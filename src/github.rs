use anyhow::Result;
use hubcaps::{Credentials, Github as GitHubClient};

pub struct CommitInfo {
    pub revision: String,
    pub branch: String,
}

pub struct ReleaseInfo {
    pub tarball_url: String,
    pub release_name: String,
}

static USER_AGENT: &'static str = concat!(env!("CARGO_PKG_NAME"), "/", env!("CARGO_PKG_VERSION"));

fn get_github_credentials_from_env() -> Option<Credentials> {
    match std::env::var("GITHUB_TOKEN") {
        Ok(v) => Some(Credentials::Token(v)),
        Err(_) => None,
    }
}

pub struct Client {
    client: GitHubClient,
}

impl Client {
    pub fn new() -> Result<Self> {
        Self::custom_host("https://api.github.com")
    }

    fn custom_host(host: impl AsRef<str>) -> Result<Self> {
        let creds = get_github_credentials_from_env();
        let client = GitHubClient::host(host.as_ref(), USER_AGENT, creds)?;

        Ok(Self { client })
    }

    pub async fn get_latest_commit(
        &self,
        owner: impl AsRef<str>,
        repo: impl AsRef<str>,
        branch: impl AsRef<str>,
    ) -> Result<CommitInfo> {
        let gh = &self.client;

        let commit = gh
            .repo(owner.as_ref(), repo.as_ref())
            .commits()
            .get(branch.as_ref())
            .await?;

        Ok(CommitInfo {
            revision: commit.sha,
            branch: branch.as_ref().to_string(),
        })
    }

    pub async fn get_latest_release(
        &self,
        owner: impl AsRef<str>,
        repo: impl AsRef<str>,
    ) -> Result<ReleaseInfo> {
        let gh = &self.client;

        let release = gh
            .repo(owner.as_ref(), repo.as_ref())
            .releases()
            .latest()
            .await?;

        Ok(ReleaseInfo {
            tarball_url: release.tarball_url,
            release_name: release.name,
        })
    }
}

